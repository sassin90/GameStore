La disponibilité des produits ainsi que le délai de livraison sont entre  2 et 3 Semaines.

Dans l’hypothèse où, postérieurement à votre commande, un produit ou un service deviendrait totalement ou partiellement indisponible, Word Game Staff vous informera par email de cette indisponibilité et vous donnera la possibilité :

    - Soit, si vous l’acceptez, d’être livré d’un produit ou d’un service d’une qualité et d’un prix équivalent à celui commandé
    - Soit d’être remboursé du prix du produit ou du service commandé



3.1 Modalités de passation des commandes

Il est possible de passer commande auprès de Word Game Staff :

    - En ligne sur le Site
    - Par téléphone au n° 06 99 99 99 99 99 (1.35 DH puis 0.34 DH / min) du lundi au vendredi de 9H à 21H et les week-ends et jours fériés de 9H à 18H)



Aucune commande adressée à Word Game Staff par courrier ou par fax ne pourra être prise en compte.

Toute passation de commande auprès de Word Game Staff nécessite la création d’un Espace Client. L'accès à votre Espace Client est conditionné à votre identification à l’aide de l’adresse email que vous avez indiquée lors de la création de votre compte ainsi que du mot de passe secret personnel et confidentiel que vous aurez choisi.
Ne communiquer pas les identifiants de votre Espace Client à des tiers. Word Game Staff ne saurait être tenue pour responsable de toute action réalisée via ou sur votre Espace Client par un tiers auquel vous auriez communiqué vos identifiants ou bien qui aurait eu accès à vos identifiants ou à votre Compte Client suite à une faute ou une négligence vous étant imputable.
Lors de la passation de votre commande, vous devez confirmer celle-ci par la technique dite du « double-clic » : c’est-à-dire, après avoir sélectionné des produits ajoutés à votre panier, vous devez contrôler et éventuellement corriger le contenu de votre panier (à savoir, notamment : identification et quantité de produits sélectionnés, prix correspondants, modalités et frais de livraison), avant de le valider, d’accepter les présentes CGV en cliquant sur le bouton « Accéder au paiement », tel qu’indiqué sur la page « Mode de livraison », et de confirmer ainsi l’acceptation de votre commande.

3.2 Enregistrement des commandes

Suite à la passation de votre commande, Word Game Staff vous en accusera réception en vous adressant un Email de Confirmation de Commande comprenant le récapitulatif de votre commande ainsi que son numéro, puis validera définitivement votre commande en vous adressant un Email de Validation de Commande.

Word Game Staff se réserve le droit de ne pas valider votre commande pour tout motif légitime, notamment dans l’hypothèse où :

    - Elle ne serait pas conforme aux présentes CGV
    - L’une de vos précédentes commandes n’aurait pas été intégralement payée à échéance,
    - Un litige relatif au paiement d’une de vos précédentes commandes serait en cours de traitement,
    - Plusieurs éléments graves et concordants feraient peser un soupçon de fraude sur votre commande.


Ces emails sont envoyés à l’adresse email que vous utilisez pour vous identifier dans votre Espace Client.
A noter : Si vous ne recevez pas l’un des deux emails ci-dessus, nous vous recommandons de prendre contact avec notre Service Clients. 
